def reverse_digits(number):
    if number == 0:
        return [0]
    
    digits = []
    
    while number > 0:
        digits.append(number % 10)
        number //= 10
    
    return digits

# Example usage
number = 12345
reversed_digits = reverse_digits(number)
print(reversed_digits)  # Output: [5, 4, 3, 2, 1]

"""1. Создаем функцию reverse_digits, которая принимает один аргумент - число.
2. Проверяем, если число равно 0, то возвращаем список [0].
3. Создаем пустой список digits, в который будем добавлять цифры числа.
4. Запускаем цикл while, который будет выполняться до тех пор, пока число больше 0.
5. Внутри цикла добавляем остаток от деления числа на 10 в конец списка digits.
6. Делим число нацело на 10, чтобы убрать последнюю цифру.
7. По завершении цикла возвращаем список digits, 
   который содержит цифры исходного числа в обратном порядке."""